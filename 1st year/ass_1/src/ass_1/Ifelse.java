package ass_1;

public class Ifelse {

	public static void main(String[] args) 
	{
		if(6>10)
		{
		    System.out.print("6 is within 10");
		}
		else if (13>10 && 13>20)
		{
			System.out.print("13 is within 10 and 20");
		}
		else if (23>20 && 23>30)
		{
			System.out.print("23 is within 20 and 30");
		}
		else 
		{
			System.out.print("Out of Range");
		}

	}

}
